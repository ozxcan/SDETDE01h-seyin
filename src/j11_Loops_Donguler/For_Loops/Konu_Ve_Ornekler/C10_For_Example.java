package j11_Loops_Donguler.For_Loops.Konu_Ve_Ornekler;

import java.util.Scanner;

public class C10_For_Example {
    public static void main(String[] args) {
        /*  -----> ÖRNEK
        41 kere, "Maşallah" kod yazdırın.

         */

        String str = "Maşallah";

        for (int i = 1; i <= 40; i++) {
            System.out.println(i+ ". kere " + str);
        }




    }
}
