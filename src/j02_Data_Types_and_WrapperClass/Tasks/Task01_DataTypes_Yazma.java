package j02_Data_Types_and_WrapperClass.Tasks;

public class Task01_DataTypes_Yazma {
    public static void main(String[] args) {

        // j06_If_Konulari.Tasks : Her bir primitive data type dan birer variable oluşturun ve bunu ekrana yazdırın

        int sayi = 51;
        System.out.println(sayi);

        double kesirli = 51.97;
        System.out.println(kesirli);

        char ch = 'H';
        System.out.println(ch);

        boolean right = false;
        System.out.println(right);

        String str = "\u001B[32mCyber Security is one of the most important concern of IT industry that we need to take into consideration.";
        System.out.println(str); // Yukarıda olan kod renk kodudur. Metnin başına direk bitişik yazılır.

        float ondalik = 29.974f;
        System.out.println(ondalik);

        byte elma = 127;
        System.out.println(elma);

        short armut = 32767;
        System.out.println(armut);

        long muz = 8954569846785412L;
        System.out.println(muz);











    }
}
